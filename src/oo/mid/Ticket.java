package oo.mid;

public class Ticket {
	int id;
	String name;
	String price;
	
	public void setId(int a){
		id = a;
	}
	public void setName(String b){
		name = b;
	}
	public void setPrice(String c){
		price = c;
	}
	public void getId(int d){
		id+=d;
	}
	public void getName(String e){
	name +=e;
	}
	public void getPrice(String f){
	price+=f;
	}
	
}
