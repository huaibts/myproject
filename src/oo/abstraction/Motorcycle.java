package oo.abstraction;

public class Motorcycle {
	String brand;
	String name;
	String color;
	int weight;
	int cc;
	
	public Motorcycle(String brand,String name,String color,int weight,int cc){
		this.brand = brand;
		this.name = name;
		this.color = color;
		this.weight = weight;
		this.cc = cc;
	}
}
